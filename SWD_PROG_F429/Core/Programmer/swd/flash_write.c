/**********************************************************
 * Erases enough pages of flash to fit a image of the 
 * given size. The size of flash pages is retrieved
 * from the target. The pages are erased by 
 * writing directly to the MSC registers 
 * 
 * @param size
 *    Size in bytes of firmware
 **********************************************************/
void erasePages(uint32_t size)
{
	const uint32_t ctrl = SWD_G0_FLASH_CR_MER1 | SWD_G0_FLASH_CR_MER2 | SWD_G0_FLASH_CR_START;
	uint32_t tmp = 0;

	/* Flash Unlock */
	printf("Flash Unlock.\n");
	writeMem(SWD_G0_FLASH_KEYR, SWD_G0_FLASH_KEYR_KEY1);
	writeMem(SWD_G0_FLASH_KEYR, SWD_G0_FLASH_KEYR_KEY2);

	/* Mass Erase */
	printf("Mass Erase.\n");
	writeMem(SWD_G0_FLASH_CR, ctrl);

	/* Wait until complete */
	while (readMem(SWD_G0_FLASH_SR) & SWD_G0_FLASH_SR_BSY_MASK)
	{
		//printf(".");
	}

	printf("\n");
	printf("Flash Operation End.\n");
	writeMem(SWD_G0_FLASH_SR, FLASH_SR_EOP);
	writeMem(SWD_G0_FLASH_CR, 0);

	printf("Flash Lock.\n");
	tmp = readMem(SWD_G0_FLASH_CR) | SWD_G0_FLASH_CR_LOCK;
	writeMem(SWD_G0_FLASH_CR, tmp);
}


/**********************************************************
 * Writes a firmware image to internal flash of the
 * target using direct writes to the MSC registers.
 * Writing starts at address 0x00 at the target
 * and data is copied from the supplied buffer
 * 
 * @param fwImage
 *    Pointer to firmware image
 *    
 * @param size
 *    Size in bytes of firmware image
 **********************************************************/
static void uploadImage(uint32_t *fwImage, uint32_t size)
{
	const uint32_t ctrl = SWD_G0_FLASH_CR_MER1 | SWD_G0_FLASH_CR_MER2 | SWD_G0_FLASH_CR_START;

	uint32_t tmp = 0;
	uint32_t startAddr = SWD_G0_FLASH_START;
	uint32_t i = 0;

	/* Flash Unlock */
	printf("Flash Unlock.\n");
	writeMem(SWD_G0_FLASH_KEYR, SWD_G0_FLASH_KEYR_KEY1);
	writeMem(SWD_G0_FLASH_KEYR, SWD_G0_FLASH_KEYR_KEY2);

	printf("Flash Busy Check.\n");
	while (readMem(SWD_G0_FLASH_SR) & SWD_G0_FLASH_SR_BSY_MASK)
	{
		printf(".\n");
	}

	printf("Flash Error Check.\n");
	if(readMem(SWD_G0_FLASH_SR) & SWD_G0_FLASH_SR_ERROR_MASK)
	{
		while(1);
	}

	printf("Flash Program.\n");
	writeMem(SWD_G0_FLASH_CR, SWD_G0_FLASH_CR_PG);

	for(i = 0; i < size/4 ; )
	{
		//printf("Address = 0x%.8x\n", startAddr+(i*4));
		writeMem(startAddr+(i*4),	fwImage[i++]);
		//printf("Address = 0x%.8x\n", startAddr+(i*4));
		writeMem(startAddr+(i*4),	fwImage[i++]);

		while (readMem(SWD_G0_FLASH_SR) & SWD_G0_FLASH_SR_BSY_MASK)
		{
			//printf(".\n");
		}
	}

	printf("Flash Operation End.\n");
	writeMem(SWD_G0_FLASH_SR, FLASH_SR_EOP);
	writeMem(SWD_G0_FLASH_CR, 0);

	printf("Flash Lock.\n");
	tmp = readMem(SWD_G0_FLASH_CR) | SWD_G0_FLASH_CR_LOCK;
	writeMem(SWD_G0_FLASH_CR, tmp);
}


